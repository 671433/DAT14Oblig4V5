using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using DAT14Oblig4V5.Models;
using System.Security.Claims;

namespace DAT14Oblig4V5.Controllers
{
    public class PeopleController : Controller
    {
        private readonly Oblig4Dat154DbContext _context;


        

        public PeopleController(Oblig4Dat154DbContext context)
        {
            _context = context;
        }

        

        // GET: People
        public async Task<IActionResult> Index()
        {
            var oblig4Dat154DbContext = _context.People.Include(p => p.RoleNavigation);
            return View(await oblig4Dat154DbContext.ToListAsync());
        }

        // GET: People/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var person = await _context.People
                .Include(p => p.RoleNavigation)
                .FirstOrDefaultAsync(m => m.PersonId.Equals(id));
            if (person == null)
            {
                return NotFound();
            }

            return View(person);
        }

        // GET: People/Create
        public IActionResult Create()
        {
            ViewData["Role"] = new SelectList(_context.Roles, "RoleId", "RoleId");
            return View();
        }

        // POST: People/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("PersonId,Surname,Lastname,Email,Phone,Password,Role")] Person person)
        {
            if (ModelState.IsValid)
            {
                _context.Add(person);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            ViewData["Role"] = new SelectList(_context.Roles, "RoleId", "RoleId", person.Role);
            return View(person);
        }

        // GET: People/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var person = await _context.People.FindAsync(id);
            if (person == null)
            {
                return NotFound();
            }
            ViewData["Role"] = new SelectList(_context.Roles, "RoleId", "RoleId", person.Role);
            return View(person);
        }

        // POST: People/Edit/5
